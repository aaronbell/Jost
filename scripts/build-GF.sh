sed -i 's/Jost 300 Light/Jost Light/g' ../sources-GF/UFO/300/300-italic.ufo/fontinfo.plist

#400
sed -i 's/Jost\*/Jost/g' ../sources-GF/UFO/400/400.ufo/fontinfo.plist
