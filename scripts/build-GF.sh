#500
sed -i 's/Jost\*/Jost/g' ../sources-GF/UFO/500/500.ufo/fontinfo.plist
sed -i 's/Jost 500 Medium/Jost Medium/g' ../sources-GF/UFO/500/500.ufo/fontinfo.plist

